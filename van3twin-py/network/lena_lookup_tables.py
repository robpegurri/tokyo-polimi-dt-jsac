# Modulation name by Q_m
_MOD_NAME = {2: "QPSK", 4: "16QAM", 6: "64QAM", 8: "256QAM"}

def get_mcs_params(mcs: int) -> dict:
    """
    Return the physical-layer parameters for a given MCS index (Table 1).
 
    Parameters
    ----------
    mcs : int
        MCS index in [0, 28].
 
    Returns
    -------
    dict with keys:
        modulation_order : int    – Q_m (2=QPSK, 4=16QAM, 6=64QAM)
        modulation       : str    – human-readable modulation name
        code_rate        : float  – effective code rate R
        spectral_eff     : float  – SE = Q_m * R  [bits/s/Hz]
    """
    if not (0 <= mcs < len(_MCS_TABLE1)):
        raise ValueError(f"MCS index must be in [0, {len(_MCS_TABLE1)-1}], got {mcs}")
    qm, ecr, se = _MCS_TABLE1[mcs]
    return {
        "modulation_order": qm,
        "modulation":       _MOD_NAME[qm],
        "code_rate":        ecr,
        "spectral_eff":     se,
    }
